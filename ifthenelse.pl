# Comparing numbers
$a = 5;
$b = 6;
if ($a > $b)
{
	print "\$a is greater than \$b \n";	
}
else
{
	print "\$a is lesser than \$b \n";
}


# Comparing strings
$x = "a"; 
$y = "A";
if ($x gt $y)
{
	print "\$x is lesser than \$y\n";
}
else
{
	print "\$x is lesser than \$y\n";
}